func TestAddRoundKey(t *testing.T) {
	state0 := [4][4]byte {
		{ 0x32, 0x43, 0xf6, 0xa8, } ,
		{ 0x88, 0x5a, 0x30, 0x8d, } ,
		{ 0x31, 0x31, 0x98, 0xa2, } ,
		{ 0xe0, 0x37, 0x07, 0x34, } ,
	}
	roundKey0 := [4][4]byte {
		{ 0x2b, 0x7e, 0x15, 0x16, } ,
		{ 0x28, 0xae, 0xd2, 0xa6, } ,
		{ 0xab, 0xf7, 0x15, 0x88, } ,
		{ 0x09, 0xcf, 0x4f, 0x3c, } ,
	}
	expectedState0 := [4][4]byte {
		{ 0x19, 0x3d, 0xe3, 0xbe, } ,
		{ 0xa0, 0xf4, 0xe2, 0x2b, } ,
		{ 0x9a, 0xc6, 0x8d, 0x2a, } ,
		{ 0xe9, 0xf8, 0x48, 0x08, } ,
	}
	if newState := AddRoundKey(state0, roundKey0); newState != expectedState0 {
		t.Errorf("Test 0:\nExpected: %v\nGiven: %v\n", expectedState0, newState)
	}

	state1 := [4][4]byte {
		{ 0x04, 0x66, 0x81, 0xe5, } ,
		{ 0xe0, 0xcb, 0x19, 0x9a, } ,
		{ 0x48, 0xf8, 0xd3, 0x7a, } ,
		{ 0x28, 0x06, 0x26, 0x4c, } ,
	}
	roundKey1 := [4][4]byte {
		{ 0xa0, 0xfa, 0xfe, 0x17, } ,
		{ 0x88, 0x54, 0x2c, 0xb1, } ,
		{ 0x23, 0xa3, 0x39, 0x39, } ,
		{ 0x2a, 0x6c, 0x76, 0x05, } ,
	}
	expectedState1 := [4][4]byte {
		{ 0xa4, 0x9c, 0x7f, 0xf2, } ,
		{ 0x68, 0x9f, 0x35, 0x2b, } ,
		{ 0x6b, 0x5b, 0xea, 0x43, } ,
		{ 0x02, 0x6a, 0x50, 0x49, } ,
	}
	if newState := AddRoundKey(state1, roundKey1); newState != expectedState1 {
		t.Errorf("Test 1:\nExpected: %v\nGiven: %v\n", expectedState1, newState)
	}
}

